# Напишите алгоритм, который проверяет, является ли строка палиндромом. Изобразите блок-схему.


def is_palindrome(s):
    s = s.replace(" ", "").lower()  # Удаляем пробелы и приводим к нижнему регистру
    return s == s[::-1]  # Сравниваем строку с её обратной версией

# Пример использования
input_str = input("Введите строку: ")
if is_palindrome(input_str):
    print("Это палиндром!")
else:
    print("Это не палиндром.")
