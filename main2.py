# Получаем три числа от пользователя и преобразуем их в тип float (числа с плавающей точкой)
a = float(input("Введите первое число: "))
b = float(input("Введите второе число: "))
c = float(input("Введите третье число: "))

# Поиск максимального из трех чисел
if a >= b and a >= c:  # Если a больше или равно b и c
    maximum = a         # то a — максимальное
elif b >= c:            # Иначе, если b больше или равно c
    maximum = b         # то b — максимальное
else:                   # Если оба условия выше не выполнены
    maximum = c         # то c — максимальное

# Выводим результат
print(f"Максимальное число: {maximum}")
