# Напишите алгоритм, который ищет заданный элемент в массиве (линейный поиск). Изобразите блок-схему.


# Определение функции линейного поиска
def linear_search(arr, target):
    # Проходим по каждому элементу массива с его индексом
    for index, element in enumerate(arr):
        # Если текущий элемент совпадает с искомым значением (target)
        if element == target:
            return index  # Возвращаем индекс элемента (поиск завершен)
    return -1  # Если элемент не найден, возвращаем -1

# Пример использования
arr = [4, 2, 9, 7, 5, 1]  # Исходный массив
target = 7                 # Элемент, который нужно найти

# Вызов функции поиска и сохранение результата
result = linear_search(arr, target)

# Проверка результата
if result != -1:
    # Вывод сообщения, если элемент найден
    print(f"Элемент {target} найден на позиции {result}.")
else:
    # Вывод сообщения, если элемент отсутствует
    print(f"Элемент {target} отсутствует в массиве.")

