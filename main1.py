# Напишите программу, которая принимает целое число и определяет, является ли оно четным или нечетным. Изобразите блок-схему.


# Запрашиваем у пользователя целое число и преобразуем ввод в тип int (целое число)
num = int(input("Введите целое число: "))

# Проверяем, делится ли число на 2 без остатка
if num % 2 == 0:
    # Если условие верно (остаток = 0), выводим сообщение о четности
    print(f"{num} — четное число")
else:
    # Если условие неверно (остаток ≠ 0), выводим сообщение о нечетности
    print(f"{num} — нечетное число")
