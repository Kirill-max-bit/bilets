# Создайте программу, которая подсчитывает количество гласных букв в заданной строке. Изобразите блок-схему.


# Функция для подсчета гласных букв в тексте
def count_vowels(text):
    # Создаем множество гласных букв для быстрой проверки
    vowels = {'a', 'e', 'i', 'o', 'u'}  # Множество содержит только уникальные элементы
    count = 0  # Счетчик гласных

    # Проходим по каждому символу в тексте, игнорируя регистр (преобразуем в нижний)
    for char in text.lower():  # text.lower() делает все символы строчными
        # Если символ есть в множестве гласных
        if char in vowels:
            count += 1  # Увеличиваем счетчик

    return count  # Возвращаем итоговое количество

# Пример использования
text = input("Введите текст: ")  # Пользователь вводит текст (например: "Hello World")
# Вызываем функцию и выводим результат в формате: "Количество гласных: X"
print(f"Количество гласных: {count_vowels(text)}") 
