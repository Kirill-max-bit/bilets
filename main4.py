#Создайте алгоритм для вычисления факториала заданного числа N. Изобразите блок-схему.


# Запрашиваем у пользователя целое число и преобразуем ввод в тип int
n = int(input("Введите целое число N: "))

# Проверяем, является ли число отрицательным
if n < 0:
    print("Факториал отрицательного числа не определен")
else:
    # Инициализируем переменную для хранения результата (0! = 1)
    factorial_result = 1
    
    # Цикл от 1 до n включительно (range(1, n+1) дает числа 1, 2, ..., n)
    for i in range(1, n + 1):
        factorial_result *= i  # Умножаем текущий результат на i
    
    # Выводим итоговый факториал
    print(f"Факториал {n}! = {factorial_result}")
