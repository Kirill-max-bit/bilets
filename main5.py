# Напишите программу, которая проверяет, является ли заданное число простым. Изобразите блок-схему.


import math  # Импорт модуля для математических операций (используем isqrt для корня)

n = int(input("Введите целое число: "))  # Получаем число от пользователя

# Проверка условий для определения простоты числа
if n <= 1:
    is_prime = False  # Числа ≤ 1 не являются простыми
elif n == 2:
    is_prime = True   # 2 — единственное четное простое число
elif n % 2 == 0:
    is_prime = False  # Все остальные четные числа не простые
else:
    is_prime = True   # Предполагаем, что число простое, пока не найдем делитель
    max_divisor = math.isqrt(n) + 1  # Верхняя граница проверки делителей (корень из n + 1)
    
    # Проверяем делители от 3 до max_divisor с шагом 2 (только нечетные)
    for i in range(3, max_divisor, 2):
        if n % i == 0:  # Если найден делитель
            is_prime = False
            break  # Выход из цикла, дальше проверять нет смысла

# Вывод результата с использованием тернарного оператора
print(f"Число {n} {'простое' if is_prime else 'не является простым'}") 
