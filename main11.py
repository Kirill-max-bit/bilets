# Создайте программу, которая конвертирует температуру из Цельсия в Фаренгейт и наоборот. Изобразите блок-схему.


# Конвертация Цельсий ↔ Фаренгейт (базовая версия)
# Запрос выбора направления конвертации у пользователя
choice = input("Конвертировать:\n1. C → F\n2. F → C\nВыберите 1/2: ")

# Получение температуры от пользователя и преобразование в число с плавающей точкой
temp = float(input("Температура: "))  

# Проверка выбора пользователя
if choice == '1':
    # Конвертация Цельсия → Фаренгейт: (C × 9/5) + 32
    # Результат округляется до одного знака после запятой
    print(f"Результат: {(temp * 9/5) + 32:.1f}°F")  
elif choice == '2':
    # Конвертация Фаренгейт → Цельсий: (F - 32) × 5/9
    # Результат округляется до одного знака после запятой
    print(f"Результат: {(temp - 32) * 5/9:.1f}°C")  
else:
    # Обработка некорректного ввода (выбрано не 1 и не 2)
    print("Ошибка: выберите 1 или 2")  
